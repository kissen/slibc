#pragma once

/**
 * Return whether c is a digit.
 */
int isdigit(int c);

/**
 * Return whether c is a whitespace character.
 */
int isspace(int c);

/**
 * Return c as an upper-case character or as-is if it has no casing.
 */
int toupper(int c);

/**
 * Return c as a lower-case character or as-is if it has no casing.
 */
int tolower(int c);

/**
 * Return wheter c is an upper case letter.
 */
int isupper(int c);

/**
 * Return wheter c is a lower case letter.
 */
int islower(int c);

/**
 * Return whether c is an ASCII character.
 */
int isascii(int c);
